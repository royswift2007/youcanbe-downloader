def normalize_lang(value):
    if value in (LANG_ZH, LANG_EN):
        return value
    return DEFAULT_LANG


def tr(key, lang=DEFAULT_LANG, fallback=""):
    lang = normalize_lang(lang)
    return TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS[DEFAULT_LANG].get(key) or fallback or key
